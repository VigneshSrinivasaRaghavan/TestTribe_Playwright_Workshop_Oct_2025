// If Block
let studentAge = 10;
if (studentAge >= 18) {
    console.log("Student is eligible for voting");
    console.log("Student is eligible for driving");
}

// If Else Block
let studentAge2 = 20;
if (studentAge2 >= 18) {
    console.log("Student is eligible for voting");
    console.log("Student is eligible for driving");
}
else {
    console.log("Student is NOT eligible for voting");
    console.log("Student is NOT eligible for driving");
}

// If Else If Block
let grade = 75;

if (grade >= 90) {
    console.log("A");
} else if (grade >= 80 && grade < 90) {
    console.log("B");
} else if (grade >= 70 && grade < 80) {
    console.log("C");
} else {
    console.log("F");
}
// Syntax of variable declaration
// keyword variableName= value;

// var keyword
var age = 20;
console.log(age);

// let keyword
let myName = "Vignesh S";
console.log(myName);

// const keyword
const gender = "Male";
console.log(gender);

// Syntax to redecalare or reassign a variable
// variableName = newValue;

age = 31;
myName = "John";
// gender = "Female";